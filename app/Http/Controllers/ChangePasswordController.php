     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(ChangePasswordRequest $request)
    {
        $changePasswordService = new ChangePasswordService();
        if($changePasswordService->ChangePasswordService($request->old_password, $request->password) == true) {
            return redirect()->route('profile.index')->with('success', ' Your Password changed successfully');
        } else {
            return redirect()->route('change-password.index')->with('error_change', 'Old password is incorrect');
        }
    }

